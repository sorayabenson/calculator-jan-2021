export function additionResult(addNumber1, addNumber2) {
    return addNumber1 + addNumber2;
}

export function subtractionResult(subNumber1, subNumber2) {
    return subNumber1 - subNumber2;
}

export function multiplicationResult(multiNumber1, multiNumber2) {
    return multiNumber1 * multiNumber2;
}

export function divisionResult(divNumber1, divNumber2) {
    return divNumber1 / divNumber2;
}